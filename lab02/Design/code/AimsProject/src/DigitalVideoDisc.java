
public class DigitalVideoDisc {
      public String title;
      public String category;
      public String director;
      public int length;
      public float cost;
	public String getTitle() {
		return title;
	}
	public String getCategory() {
		return category;
	}
	public String getDirector() {
		return director;
	}
	public int getLength() {
		return length;
	}
	public float getCost() {
		return cost;
	}
      
      
}
